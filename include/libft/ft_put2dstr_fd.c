#include "libft.h"

void	ft_put2dstr_fd(char **str, int fd)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putstr_fd(str[i], fd);
		write(2, "\n", 1);
		i++;
	}
}
